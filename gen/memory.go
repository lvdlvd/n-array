package main

// Memory linker-script emission. From MEMORY.periph and a target name that
// encodes the part and run model, narray emits the device .ld: the MEMORY
// regions, the REGION_ALIAS bindings that let one generic lib/sections.ld serve
// both run models, the stack top, and the INCLUDEs.
//
// The target name carries the part: STM32G473Exx_ROM means family G473 (so
// Cat3, selecting the SRAM map), flash suffix E (512 KB), run model ROM. The
// "xx" wildcards package and temperature, which do not affect memory.
//
//   ROM (base case): code/vectors in FLASH, .data loaded from FLASH into SRAM1.
//   RAM: a CODE hole carved from SRAM1 holds code/vectors; .data is in place.
//
// One lib/sections.ld and one boot work for both — only the aliases differ.

import (
	"fmt"
	"io"
	"sort"
	"strings"
)

type memTarget struct {
	part    string // STM32G473Exx
	model   string // ROM or RAM
	variant *Variant
	flashKB int
}

// parseMemTarget decodes a target like "STM32G473Exx_ROM" against the known
// variants: the longest family the part name starts with, then the flash suffix.
func (d *Device) parseMemTarget(name string) (memTarget, error) {
	name = strings.TrimSuffix(name, ".ld")
	part, model := name, "ROM"
	if i := strings.LastIndex(name, "_"); i >= 0 {
		part, model = name[:i], strings.ToUpper(name[i+1:])
	}
	if model != "ROM" && model != "RAM" {
		return memTarget{}, fmt.Errorf("target %q: model must be ROM or RAM, got %q", name, model)
	}

	best := d.matchVariant(part)
	if best == nil {
		return memTarget{}, fmt.Errorf("target %q: no memory variant matches", name)
	}

	body := strings.TrimPrefix(part, "STM32")
	suffix := strings.TrimPrefix(body, best.Family) // "Exx" -> first char is the flash code
	if suffix == "" {
		return memTarget{}, fmt.Errorf("target %q: missing flash suffix after %s", name, best.Family)
	}
	code := suffix[:1]
	kb := 0
	for _, s := range best.Flash.Sizes {
		if s.Suffix == code {
			kb = s.KB
		}
	}
	if kb == 0 {
		return memTarget{}, fmt.Errorf("target %q: flash suffix %q not valid for %s", name, code, best.Family)
	}
	return memTarget{part: part, model: model, variant: best, flashKB: kb}, nil
}

func (v *Variant) region(name string) *Region {
	for i := range v.SRAM.Regions {
		if v.SRAM.Regions[i].Name == name {
			return &v.SRAM.Regions[i]
		}
	}
	return nil
}

func writeMemoryLD(w io.Writer, d *Device, name string) error {
	t, err := d.parseMemTarget(name)
	if err != nil {
		return err
	}
	v := t.variant
	p := func(format string, a ...any) { fmt.Fprintf(w, format, a...) }

	// regions sorted by address for readability
	var regs []*Region
	for i := range v.SRAM.Regions {
		regs = append(regs, &v.SRAM.Regions[i])
	}
	sort.SliceStable(regs, func(i, j int) bool { return regs[i].Base < regs[j].Base })

	p("/* %s — Cat%s, %dK flash, %s — run from %s.\n", t.part, v.Category, t.flashKB, v.Description, t.model)
	p("   Generated by narray from MEMORY.periph, DO NOT EDIT. */\n\n")

	p("MEMORY\n{\n")
	p("  FLASH  (rx) : ORIGIN = %s, LENGTH = %dK\n", v.Flash.Base.Hex(), t.flashKB)
	for _, r := range regs {
		p("  %-6s (rw) : ORIGIN = %s, LENGTH = %dK", r.Name, r.Base.Hex(), r.SizeKB)
		if r.Alias != 0 {
			p("  /* aliased at %s */", r.Alias.Hex())
		}
		p("\n")
	}
	p("}\n\n")

	// REGION_ALIAS lets a generic lib sections file place sections without
	// knowing the run model. ROM splits code (TEXT=FLASH) from data (DATA=SRAM1)
	// with the .data image loaded from LOAD=FLASH. RAM packs everything into one
	// region — a single alias RUN, because two aliases to one region get
	// independent position counters and would overlap.
	sections := "sections.ld"
	if t.model == "ROM" {
		p("REGION_ALIAS(\"TEXT\", FLASH);  /* code & vectors in flash           */\n")
		p("REGION_ALIAS(\"DATA\", SRAM1);  /* .data/.bss run in SRAM            */\n")
		p("REGION_ALIAS(\"LOAD\", FLASH);  /* .data initialiser image in flash */\n")
	} else {
		p("REGION_ALIAS(\"RUN\", SRAM1);   /* code + data packed in SRAM, in place */\n")
		sections = "sections_ram.ld"
	}

	// stack at the top of CCMRAM (fastest, I/D-bus); fall back to last region.
	stackReg := "CCMRAM"
	if v.region(stackReg) == nil {
		stackReg = regs[len(regs)-1].Name
	}
	p("\n_estack = ORIGIN(%s) + LENGTH(%s); /* top of stack */\n\n", stackReg, stackReg)

	p("INCLUDE devs.ld      /* peripheral addresses (narray) */\n")
	p("INCLUDE %s /* section layout (lib)          */\n", sections)
	return nil
}
