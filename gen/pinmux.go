package main

// pinmux.h emission. The datasheet's alternate-function table (PINMUX.periph)
// becomes a self-contained header of pin constants: the GPIO_Pin port/pin
// encoding, the GPIO_Conf configuration bits, and — the point — one
// datasheet-validated constant per legal (pin, function) pair, e.g.
//
//	PA9_USART1_TX  ->  PA9 with AF7 baked in
//
// A board declares its pinout as an array of these OR'd with config flags; a
// wrong pin/function pair simply doesn't exist, so it's a compile error rather
// than a silent runtime miswire (the old GPIO_AF7_USART1 mnemonics named the AF
// number after one peripheral and forced you to know the real per-pin mapping).
//
// Encoding: a pinconf_t is 64 bits. The low 32 are a GPIO_Pin (port one-hot in
// [31:24], port index in [19:16], pin mask in [15:0]) so pins of one port still
// OR together; the high 32 are a GPIO_Conf (mode/af/otype/pupd/speed). Config
// flags are pre-shifted into the high word so they OR in without a wrapper.
//
// The runtime that consumes this (gpioConfig, digitalHi/Lo) is hand-written
// lib/gpio.h, which #includes the generated pinmux.h.

import (
	"fmt"
	"io"
	"sort"
	"strings"
)

func writePinmux(w io.Writer, d *Device) {
	p := func(format string, a ...any) { fmt.Fprintf(w, format, a...) }

	ports := gpioPorts(d)

	p("#pragma once\n\n")
	p("// GPIO pin map for %s (%s rev %s).\n", d.Name, d.ReferenceManual, d.Revision)
	p("// Generated by narray from PINMUX.periph (datasheet AF tables), DO NOT EDIT.\n\n")
	p("#include <stdint.h>\n\n")
	p("typedef uint64_t pinconf_t; // low 32: GPIO_Pin (port+mask), high 32: GPIO_Conf\n\n")

	writePinEnum(w, ports)
	writeConfEnum(w)

	// config flags, the GPIO_Conf bits pre-shifted into the high word so they
	// OR straight onto a pin. 64-bit enum constants: standard in C23, a
	// pedantic-only warning before it.
	p("\nenum GPIO_Flag {\n")
	for _, f := range []struct{ name, conf string }{
		{"PIN_INPUT", "GPIO_INPUT"}, {"PIN_OUTPUT", "GPIO_OUTPUT"}, {"PIN_ANALOG", "GPIO_ANALOG"},
		{"PIN_OPENDRAIN", "GPIO_OPENDRAIN"}, {"PIN_PULLUP", "GPIO_PULLUP"}, {"PIN_PULLDOWN", "GPIO_PULLDOWN"},
		{"PIN_LOW", "GPIO_LOW"}, {"PIN_MEDIUM", "GPIO_MEDIUM"}, {"PIN_FAST", "GPIO_FAST"}, {"PIN_HIGH", "GPIO_HIGH"},
	} {
		p("\t%-13s = (pinconf_t)%s << 32,\n", f.name, f.conf)
	}
	p("};\n")

	writeFuncConstants(w, d, ports)

	// pinfor(mux): recover the bare pin (port + mask, low 32 bits) from a
	// GPIO_Mux constant, e.g. to drive digitalHi/Lo on a pin you configured via
	// its mux value. The high 32 (AF/mode) are dropped.
	p("\nstatic inline enum GPIO_Pin pinfor(enum GPIO_Mux m) { return (enum GPIO_Pin)(uint32_t)m; }\n")
}

// gpioPorts returns the GPIO instance ids actually present (A..G on G4).
func gpioPorts(d *Device) []string {
	gp, err := d.peripheral("GPIO")
	if err != nil {
		return nil
	}
	var ports []string
	for _, in := range gp.Instances {
		ports = append(ports, in.ID)
	}
	return ports
}

func writePinEnum(w io.Writer, ports []string) {
	p := func(format string, a ...any) { fmt.Fprintf(w, format, a...) }

	p("enum GPIO_Pin {\n")
	for i := 0; i < 16; i++ {
		p("\tPin_%-2d = 0x%04X,\n", i, 1<<i)
	}
	p("\tPin_All = 0xFFFF,\n\n")
	// GPIO_<port> = one-hot port bit in [31:24] | port index in [19:16].
	// OR-ing pins of one port keeps a single one-hot bit; mixing ports sets
	// two, which the runtime detects.
	for i, id := range ports {
		p("\tGPIO_%s = 0x%08X,\n", id, (1<<(24+i))|(i<<16))
	}
	p("\n")
	for _, id := range ports {
		var b strings.Builder
		for n := 0; n < 16; n++ {
			fmt.Fprintf(&b, "\tP%s%-2d = GPIO_%s | Pin_%d,", id, n, id, n)
		}
		p("%s\n", b.String())
		fmt.Fprintf(w, "\tP%sAll = GPIO_%s | Pin_All,\n", id, id)
		if id == "A" {
			// SWDIO=PA13, SWCLK=PA14 across essentially the whole STM32 line.
			// "Most" is the port minus those, so a board can blanket its unused
			// pins to analog without dropping the debugger off a running chip.
			fmt.Fprintf(w, "\tPAMost = GPIO_A | (Pin_All & ~(Pin_13 | Pin_14)), // port A minus SWDIO(PA13)/SWCLK(PA14)\n")
		}
	}
	p("};\n")
}

func writeConfEnum(w io.Writer) {
	io.WriteString(w, `
enum GPIO_Conf {
	GPIO_INPUT  = 0x0000,
	GPIO_OUTPUT = 0x1000,
	GPIO_AFOUT  = 0x2000,
	GPIO_ANALOG = 0x3000,

	GPIO_OPENDRAIN = 0x0080, // OTYPE, output/af only (0 = push-pull)

	GPIO_PULLUP   = 0x0010, // PUPD
	GPIO_PULLDOWN = 0x0020,

	GPIO_LOW    = 0x0000, // OSPEED, output/af only
	GPIO_MEDIUM = 0x0001,
	GPIO_FAST   = 0x0002,
	GPIO_HIGH   = 0x0003,
};
`)
}

// writeFuncConstants emits one #define per legal (pin, function) from the
// datasheet, baking in the AF number. Category-restricted entries carry a note.
func writeFuncConstants(w io.Writer, d *Device, ports []string) {
	p := func(format string, a ...any) { fmt.Fprintf(w, format, a...) }

	present := map[string]bool{}
	for _, id := range ports {
		present[id] = true
	}

	pins := append([]*Pin{}, d.Pins...)
	sort.SliceStable(pins, func(i, j int) bool { return pins[i].Name < pins[j].Name })

	p("\n// Datasheet (pin, function) -> pin with the correct AF baked in. A pair\n")
	p("// that does not exist here is not a legal mux: using it is a compile error.\n")
	p("enum GPIO_Mux {\n")
	for _, pin := range pins {
		port := strings.TrimPrefix(pin.Name, "P")
		if len(port) > 0 && !present[port[:1]] {
			continue
		}
		seen := map[string]bool{}
		for _, af := range pin.AFs {
			fn := cIdent(af.Func)
			if fn == "" || af.Func == "-" || seen[fn] {
				continue
			}
			seen[fn] = true
			name := fmt.Sprintf("%s_%s", pin.Name, fn)
			p("\t%-24s = (pinconf_t)%-5s | ((pinconf_t)(GPIO_AFOUT | (%2d << 8)) << 32),", name, pin.Name, af.Num)
			if af.Cat != "" {
				p(" // Cat%s only", af.Cat)
			}
			p("\n")
		}
	}
	p("};\n")
}

// cIdent makes a function name a safe C identifier fragment, or "" if it has no
// usable characters (the "-" placeholder).
func cIdent(s string) string {
	var b strings.Builder
	for _, r := range s {
		switch {
		case r >= 'A' && r <= 'Z', r >= 'a' && r <= 'z', r >= '0' && r <= '9', r == '_':
			b.WriteRune(r)
		default:
			b.WriteRune('_')
		}
	}
	out := strings.Trim(b.String(), "_")
	return out
}
